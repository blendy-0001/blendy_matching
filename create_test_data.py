def create_member(data):
    """Notion にメンバーを追加"""
    url = f"https://api.notion.com/v1/pages"

    properties = {
        "名前": {"title": [{"text": {"content": data["名前"]}}]},
        "会社名": {"rich_text": [{"text": {"content": data["会社名"]}}]},
        "業種カテゴリ": {"select": {"name": data["業種カテゴリ"]}},
        "業種詳細": {"rich_text": [{"text": {"content": data["業種詳細"]}}]},
        "主力サービス": {"rich_text": [{"text": {"content": data["主力サービス"]}}]},
        "エンドクライアント業界": {"rich_text": [{"text": {"content": data["エンドクライアント業界"]}}]},
        "エンドクライアント規模": {"select": {"name": data["エンドクライアント規模"]}},
        "クライアントの課題": {"rich_text": [{"text": {"content": data["クライアントの課題"]}}]},
        "バリューチェーン位置": {"multi_select": [{"name": item} for item in data["バリューチェーン位置"]]},
        "強み": {"rich_text": [{"text": {"content": data["強み"]}}]},
        "課題・足りないもの": {"rich_text": [{"text": {"content": data["課題・足りないもの"]}}]},
        "保有アセット": {"multi_select": [{"name": item} for item in data["保有アセット"]]},
        "事業フェーズ": {"select": {"name": data["事業フェーズ"]}},
        "LINE ID": {"rich_text": [{"text": {"content": data["LINE ID"]}}]},
        "ステータス": {"select": {"name": "アクティブ"}},
    }

    payload = {
        "parent": {"database_id": MEMBERS_DB_ID},
        "properties": properties
    }

    response = requests.post(url, headers=HEADERS, json=payload)
    return response.json()


